def average(Ln):
    total = sum(Ln)
    cnt = len(Ln)
    avg = total / cnt
    return avg

mylist = [4, 8, 10, 6]
print("Average is:", average(mylist))

def total_wds(words):
    total = 0
    for w in words:
        total += len(w)
    return total
my_list = ["Science", "Cyber", "Database"]
print("Total letters:", total_wds(my_list))

