def keep_consonants(word):
    vowels = "aeiouAEIOU"
    result = ""
    for char in word:
        if char.isalpha() and char not in vowels:
            result += char
    return result

# ðŸ§ª Example
print(keep_consonants("babas"))     # Output: bbs
print(keep_consonants("Hello"))     # Output: Hll
print(keep_consonants("PYTHON!"))   # Output: PYTHN

def keep_consonants_from_input():
    word = input("Enter a word: ")
    return keep_consonants(word)

print(keep_consonants_from_input())

